/** Flat lookup map: name/alias â†’ emoji character */
const NAME_TO_EMOJI: Map<string, string> = new Map();

for (const cat of EMOJI_CATEGORIES) {
  for (const entry of cat.emojis) {
    NAME_TO_EMOJI.set(entry.name, entry.emoji);
    for (const alias of entry.aliases ?? []) {
      NAME_TO_EMOJI.set(alias, entry.emoji);
    }
  }
}

/** Replace :name: patterns in text with actual emoji characters. */
export function parseEmoji(text: string): string {
  return text.replace(/:([a-zA-Z0-9_+\-]+):/g, (match, name) => {
    return NAME_TO_EMOJI.get(name) ?? match;
  });
}

/** Search emojis by name prefix, returning up to `limit` matches. */
export function searchEmoji(query: string, limit = 20): EmojiEntry[] {
  const q = query.toLowerCase();
  const results: EmojiEntry[] = [];
  for (const cat of EMOJI_CATEGORIES) {
    for (const entry of cat.emojis) {
      if (entry.name.includes(q) || entry.aliases?.some((a) => a.includes(q))) {
        results.push(entry);
        if (results.length >= limit) return results;
      }
    }
  }
  return results;
}
